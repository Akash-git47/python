#Q2.Create two lists: a = [1, 2, 3], b = [4, 5, 6]

#Perform:
#Concatenation
#Repetition (repeat list a 3 times)
#Membership check (check if 2 is in list a)
#Length of list
a = [1,2,3]
b = [4,5,6]
#Concatenation
c = a + b   
print("Concatenation:", c)
#Repetition
d = a * 3
print("Repetition:", d)
#Membership check
is_in_a = 2 in a
print("Is 2 in list a?", is_in_a)
#Length of list
length_a = len(a)
print("Length of list a:", length_a)
